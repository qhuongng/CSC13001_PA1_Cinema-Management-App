create database cinemaManagement
use cinemaManagement;

create table Users (
  userId int not null identity(1,1) primary key,
  userName varchar(50) not null,
  password varchar(60) not null,
  DOB date not null,
  isAdmin BIT not null
);

create table Actor (
	actorId int not null identity(1,1) primary key,
	actorName nvarchar(50) not null,
	avatar varbinary(max),
	shotDes nvarchar(max)
);

create table Director (
	directorId int not null identity(1,1) primary key,
	directorName nvarchar(50) not null,
	avatar varbinary(max),
	shotDes nvarchar(max)
);

create table Genre (
	genreId int not null identity(1,1) primary key,
	genreName nvarchar(30) not null
);

create table AgeRating(
	ageId varchar(3) not null primary key,
	ageDef nvarchar(50) not null
);

create table Movie( -- actors and genre is a list so not include in this table
	movieId int not null identity(1,1) primary key,
	movieName nvarchar(50) not null,
	duration int not null,
	releaseYear int not null,
	IMDbRating float not null,
	directorId int not null,
	poster varbinary(max),
	certification varchar(3) not null,
	foreign key (directorId) references Director(directorId),
	foreign key(certification) references AgeRating(ageId)
);

create table MovieGenres (
	movieId int not null,
	genreId int not null,
	primary key (movieId,genreId),
	foreign key (movieId) references Movie(movieId),
	foreign key (genreId) references Genre(genreId),
)

create table MovieActors (
	movieId int not null,
	actorId int not null,
	primary key (movieId,actorId),
	foreign key (movieId) references Movie(movieId),
	foreign key (actorId) references Actor(actorId),
)

create table MovieInfos (
	movieId int not null primary key,
	isSelling bit not null,
	dailyShowtime int not null,
	weeklyShowtime int not null,
	monthlyShowtime int not null,
	soldTicket int not null,
	ticketRevenue bigint not null,
	foreign key (movieId) references Movie(movieId)
)

create table Projector (
	projectorId int identity(1,1) primary key,
	projectorInfo datetime not null,
)

create table Ticket (
	ticketId int not null identity(1,1) primary key,
	userId int not null,
	movieId int not null,
	seatId char(3) not null,
	price float not null,
	projectorId int not null,
    FOREIGN KEY (userId) REFERENCES Users(userId),
    FOREIGN KEY (movieId) REFERENCES Movie(movieId),
	FOREIGN KEY (projectorId) REFERENCES Projector(projectorId)
)

create table Voucher (
	voucherId int identity(100,1) primary key,
	userId int not null,
	isUsed bit not null,
	discountPercent int not null,
	foreign key (userId) REFERENCES Users(userId)
)

